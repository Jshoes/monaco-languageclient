// support all basic-languages
import "monaco-editor/esm/vs/basic-languages/monaco.contribution";

import * as monaco from "monaco-editor/esm/vs/editor/editor.api";

import { buildWorkerDefinition } from "monaco-editor-workers";
buildWorkerDefinition(
  "../../../../node_modules/monaco-editor-workers/dist/workers",
  import.meta.url,
  false
);

import {
  MonacoLanguageClient,
  MessageConnection,
  CloseAction,
  ErrorAction,
  MonacoServices,
  createConnection,
} from "monaco-languageclient";
import { listen } from "@codingame/monaco-jsonrpc";
import normalizeUrl from "normalize-url";

const languageId = "sql";
// register Monaco languages
monaco.languages.register({
  id: languageId,
  extensions: [".sql"],
  aliases: ["SQL", "sql"],
  mimetypes: ["application/json"],
});

// create Monaco editor
const value = "SELECT * FROM TABLE_NAME";

monaco.editor.create(document.getElementById("container")!, {
  model: monaco.editor.createModel(
    value,
    languageId,
    monaco.Uri.parse("inmemory://model.json")
  ),
  glyphMargin: true,
  lightbulb: {
    enabled: true,
  },
});

// install Monaco language client services
MonacoServices.install(monaco);

// create the web socket
const url = createUrl("localhost", 3000, "/sampleServer");
const webSocket = new WebSocket(url);

// listen when the web socket is opened
listen({
  webSocket,
  onConnection: (connection) => {
    // create and start the language client
    const languageClient = createLanguageClient(connection);
    const disposable = languageClient.start();
    connection.onClose(() => disposable.dispose());

    console.log(`Connected to "${url}" and started the language client.`);
  },
});

function createLanguageClient(
  connection: MessageConnection
): MonacoLanguageClient {
  return new MonacoLanguageClient({
    name: "SQL Language Client",
    clientOptions: {
      // use a language id as a document selector
      documentSelector: ["sql"],
      // disable the default error handler
      errorHandler: {
        error: () => ErrorAction.Continue,
        closed: () => CloseAction.DoNotRestart,
      },
    },
    // create a language client connection from the JSON RPC connection on demand
    connectionProvider: {
      get: (errorHandler, closeHandler) => {
        return Promise.resolve(
          createConnection(connection, errorHandler, closeHandler)
        );
      },
    },
  });
}

function createUrl(hostname: string, port: number, path: string): string {
  const protocol = location.protocol === "https:" ? "wss" : "ws";
  return normalizeUrl(`${protocol}://${hostname}:${port}${path}`);
}
